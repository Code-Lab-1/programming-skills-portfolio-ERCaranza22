# Colors in Japanese

color = "green"

if color == "red":
    print("aka")
elif color == "orange":
    print("orenji")
elif color == "yellow":
    print("kiiro")
elif color == "green":
    print("midori")
elif color == "blue":
    print("aoi")
elif color == "purple":
    print("murasakino")

else:
    print("none")